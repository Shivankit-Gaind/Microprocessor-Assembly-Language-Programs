.Model Tiny
.386
.DATA
	FILE	DB	'ABCD.txt',0
	STORE	DB	100 DUP('0')
	
.CODE
.Startup
	
	;OPENING THE FILE
	LEA DX, FILE 
	MOV AH, 3DH
	MOV AL, 00H ;READING
	INT 21H
	
	;READING DATA FROM THE FILE
	MOV BX, AX
	MOV AH, 3FH	
	MOV	CX,	37
	LEA	DX, STORE 
	INT 21H
	
	MOV DI, DX
	MOV AH, '$'
	MOV [DI+37],AH
	
	;DISPLAY THE DATA
	MOV AH, 09H
	LEA DX, STORE
	INT 21H
	
	;CLOSING THE FILE	
	MOV AH, 3EH 
	INT 21H
	
.EXIT
END