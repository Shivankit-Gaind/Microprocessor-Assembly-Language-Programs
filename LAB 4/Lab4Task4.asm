.Model Tiny
.386
.DATA
	FILE	DB	'ABCD.txt',0
	STORE	DB	100 DUP('0')
	
.CODE
.Startup
	
	;OPENING THE FILE
	LEA DX, FILE 
	MOV AH, 3DH
	MOV AL, 00H ;READING
	INT 21H
	
	;READING DATA FROM THE FILE BYTE BY BYTE
	MOV BX, AX
	LEA	DX, STORE
	
X1:	MOV	CX,	01
	MOV AH, 3FH	
	INT 21H
	INC DX
	CMP	AX,00H
	JNZ X1
	
	MOV DI, DX
	MOV AH, '$'
	MOV [DI-1],AH
	
	;DISPLAY THE DATA
	MOV AH, 09H
	LEA DX, STORE
	INT 21H
		
	;CLOSING THE FILE	
	MOV AH, 3EH 
	INT 21H
	
.EXIT
END